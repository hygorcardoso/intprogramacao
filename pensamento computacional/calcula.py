# Receber dois números do usuário
numero1 = float(input("Digite o primeiro número: "))
numero2 = float(input("Digite o primeiro número: "))

# Calcular a média
media = (numero1 + numero2) / 2

# Imprimir a média

print(f"A média dos dois número é: {media}") # O f no inicio do print é para indicar que vai ser impresso um flot
